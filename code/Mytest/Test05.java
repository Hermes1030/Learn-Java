// 百钱百鸡
// 鸡翁 5文钱1只；鸡雏 1文钱3只；鸡母 3文钱1只；

// x+y+z=100
// 5x+3y+z/3=100
// 思路：
// 第一层循环，用户表示鸡翁的范围，初始化表达式的变量定义为x=0，判断条件x<=20;
// 第二层循环，用户表示鸡母的范围，初始化表达式的变量定义为y=0，判断条件y<=33;
// 这时鸡雏的变量 z=100-x-y
// 判断表达式z%3==0和表达式5x+3y+z/3=100是否同时成立，如果成立，输出对应的xyz
public class Test05{
	public static void main(String[] args){
		for(int x=0;x<=20;x++){
			for(int y=0;y<=33;y++){
				int z = 100 - x - y;

				if(z%3==0 && 5*x+3*y+z/3==100){
					System.out.println("x:"+x+"y:"+y+"z:"+z);
				}
			}
		}

	}
}