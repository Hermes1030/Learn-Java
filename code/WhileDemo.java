public class WhileDemo {
    public static void main(String[] args) {
        // for 循环实现 输出5次hello world
        for(int i = 1;i<=5;i++){
            System.out.println("Hello world");
        }
        // while 循环实现 
        int a = 1;
        while(a<=5){
            System.out.println("Hello world");
            a++;
        }
    }   
}
